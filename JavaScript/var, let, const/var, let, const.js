// var
/*
- You can access a var variable bfore intilization as long as the var variable is eventually initialized anywhere in the code.
- You can declare and redeclare the same var variable without errors.
*/

// let
/*
- You cannot access a let variable before you declare it, but you can declare an unassigned variable with let.
- You can't redeclare a let variable, but you can re-assign it.
*/

// const
/*
- The const variable must be intialized.
- You can't access the const variable befroe initialization.
- You can't redeclare a const variable.

*/
